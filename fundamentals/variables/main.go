package main

import "fmt"

func main() {

	//Strings (string)
	var name string = "Kyle" // or name := "Kyle" -> Type inference
	fmt.Println(name)

	//Integer (int)
	var age int = 26 // or age := 26 -> Type inference
	fmt.Println(age)

	//Float (float64)
	var height float64 = 1.75 // or height := 1.75 -> Type inference
	fmt.Println(height)

	//Boolean (bool)
	var isMarried bool = false // or isMarried := false -> Type inference
	fmt.Println(isMarried)
}
